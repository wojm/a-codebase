
/**
 * Switches are a generaliztion on if...else that allow us to continue into the next block
 * 
 * https://developer.mozilla.org/en-US/docs/Web/JavaScript/Reference/Statements/switch
 */

const num = 7;
switch(num) {
case 0:
	console.log('january');
case 1:
	console.log('february');
case 2:
	console.log('march');
case 3:
	console.log('april');
case 4:
	console.log('may');
case 5:
	console.log('june');
case 6:
	console.log('july');
case 7:
	console.log('august');
case 8:
	console.log('september');
case 9:
	console.log('october');
case 10:
	console.log('november');
case 11:
	console.log('december');
}
// august
// september
// october
// november
// december

